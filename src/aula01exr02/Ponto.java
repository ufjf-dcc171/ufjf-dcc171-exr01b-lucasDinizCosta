package aula01exr02;

public class Ponto {
    Double x;
    Double y;

    public Ponto(Double x, Double y) {
        this.x = x;
        this.y = y;
    }

    public Ponto() {
    }

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }
}
